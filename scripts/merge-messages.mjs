// Deep-merge `src` into `dest`, only ADDING keys that are absent in `dest`.
// Arrays and primitives are treated as leaf values (added only if key missing).
function fillMissing(dest, src) {
  for (const [key, val] of Object.entries(src)) {
    if (
      val &&
      typeof val === 'object' &&
      !Array.isArray(val) &&
      dest[key] &&
      typeof dest[key] === 'object' &&
      !Array.isArray(dest[key])
    ) {
      fillMissing(dest[key], val)
    } else if (!(key in dest)) {
      dest[key] = val
    }
  }
}

let touched = 0
for (const locale of LOCALES) {
  const file = join(MESSAGES_DIR, `${locale}.json`)
  const json = JSON.parse(readFileSync(file, 'utf8'))
  fillMissing(json, data[locale])
  writeFileSync(file, JSON.stringify(json, null, 2) + '\n', 'utf8')
  touched++
  console.log(`merged → ${locale}.json`)
}
